print("KonversI Suhu dari Celcius ke Kelvin, Reamur dan Farenheit")
celcius = float(input("Masukkan Suhu dalam Celcius : "))
kelvin = celcius + 273.15
reamur = celcius * 4/5
farenheit = celcius * 9/5 + 32
print(f"Hail konversi dari suhu derajat {celcius} Celcius ke Kelvin adalah : {kelvin}K")
print(f"Hail konversi dari suhu derajat {celcius} Celcius ke Reamur adalah : {reamur}R")
print(f"Hail konversi dari suhu derajat {celcius} Celcius ke Kelvin adalah : {farenheit}F")